function addEndListener(node, eventName, eventListener) {
  node.addEventListener(eventName, eventListener, false);
}

function removeEventListener(node, eventName, eventListener) {
  node.removeEventListener(eventName, eventListener, false);
}

function one(node, eventName, eventListener){
    node.addEventListener(eventName, function(e){
      eventListener.call(node,e);
      removeEventListener(node,eventName,eventListener);
    }, false);
}

var TransitionEvents = {
  addEndEventListener:function(node, eventListener) {
    if (endEvents.length === 0) {
      window.setTimeout(eventListener, 0);
      return;
    }
    endEvents.forEach(function(endEvent) {
      addEndListener(node, endEvent, eventListener);
    });
  },

  removeEndEventListener:function(node, eventListener) {
    if (endEvents.length === 0) {
      return;
    }
    endEvents.forEach(function(endEvent) {
      removeEventListener(node, endEvent, eventListener);
    });
  }
};
